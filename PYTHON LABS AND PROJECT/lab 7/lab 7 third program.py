# Input dictionary
dict_num = {1: 10, 2: 20, 3: 30, 4: 40, 5: 50, 6: 60}

# Print key, value, and item for each key-value pair in the dictionary
for key, value in dict_num.items():
    print("Key:", key)
    print("Value:", value)
    print("Item:", (key, value))
    print()  # Add a newline for clarity
